
public class DefaultValue 
{
	
	private int i;
	private float f;
	private char c;
	private double d;
	private byte b;
	private short s;
	private long l;
	private boolean bo;
	
	public static void main(String args[])
	{
		DefaultValue a= new DefaultValue();
		
		System.out.println("Def Value of INT ="+a.i);
		System.out.println("Def Value of Float ="+a.f);
		System.out.println("Def Value of Char ="+a.c);
		System.out.println("Def Value of Double ="+a.d);
		System.out.println("Def Value of Byte ="+a.b);
		System.out.println("Def Value of Short ="+a.s);
		System.out.println("Def Value of Long ="+a.l);
		System.out.println("Def Value of Boolean ="+a.bo);
		
		
		
		
		
	}
}
